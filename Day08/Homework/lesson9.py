# 1. მომხმარებლის დაბადების წლის შემოტანა და integer-ად გადაკეთება
birth_year = input("Enter your birth year: ")  # ვთხოვთ მომხმარებელს დაბადების წლის შეყვანას
birth_year = int(birth_year)  # სტრინგის integer-ად გადაკეთება

# ვაკეთებთ integer-ად, რადგან შემდგომში შეიძლება დაგვჭირდეს რიცხვითი გამოთვლები, როგორიცაა ასაკის გამოთვლა.

# 2. მომხმარებლის username-ის შემოტანა და გამოტანა
username = input("Enter your username: ")  # ვთხოვთ მომხმარებელს username-ის შეყვანას
print("Hello, " + username + "!")  # ვაპრინტებთ მისალმებას და username-ს

# ამ კოდში ხდება სტრინგების concatenation (კონკატენაცია).

# Concatenation (კონკატენაცია) არის ორი ან მეტი სტრინგის ერთმანეთზე შერწყმა.
# მაგალითად, "Hello, " + "World!" შეერთების შემდეგ გვაძლევს "Hello, World!".

# 4. მონაცემთა ტიპები
# Python-ში მონაცემთა ტიპები არის:
# 1. int (მთლიანი რიცხვი)
