# 3. str (სტრინგი)
# 4. bool (ლოგიკური მნიშვნელობა: True ან False)
# 5. list (სია)

# 5. ფუნქციების type(), int() და str() ახსნა
# type() ფუნქცია აბრუნებს გადაცემული მნიშვნელობის მონაცემთა ტიპს.
# მაგალითად, type(123) დააბრუნებს <class 'int'>.

# int() ფუნქცია სტრინგს ან სხვა მონაცემთა ტიპს გარდაქმნის მთლიანი რიცხვად.
# მაგალითად, int("123") დააბრუნებს 123.

# str() ფუნქცია სტრინგად გარდაქმნის გადაცემულ მნიშვნელობას.
# მაგალითად, str(123) დააბრუნებს "123".